            <h1 class="text-primary">Our Founders</h1>
            <p class="small">Dominic Russo is a speaker, a humanitarian, and an innovator in international missions. At the young age of 20, Dominic founded Missions.Me and began organizing strategic outreach campaigns in cities across Latin America. These multifaceted campaigns include hundreds of missionary volunteers, large-scale relief projects, and collaborative partnerships with local political and religious leaders.</p>
            <p class="small">In 2011, Dominic launched the <a href="http://1nation1day.com" target="_blank">“1Nation1Day”</a> vision in <a href="http://1nation1day.com/honduras" target="_blank">Honduras</a> – a project that attracted the largest foreign missions team in history and was officially declared a national holiday by the Honduran President. On the week of July 20, 2013 more than 1 million Hondurans were personally reached through the <a href="http://1nation1day.com" target="_blank">1Nation1Day</a> outreaches and every national newspaper announced “We Have A New Honduras.” In 2015, the <a href="http://1nation1day.com" target="_blank">1Nation1Day</a> vision spread to the <a href="http://1nation1day.com/dominican" target="_blank">Dominican Republic</a>. The world watched again as <a href="http://1nation1day.com" target="_blank">1Nation1Day</a> forged top-level government partnerships and the national church united for the salvation of their nation. Then in 2017, the momentum around the globe erupted - mobilizing our largest team to date. 2,858 missionaries representing over 100 different churches from 33 nations and five continents descended upon the nation of <a href="http://1nation1day.com/nicaragua" target="_blank">Nicaragua</a>. Since the birth of this movement, <a href="http://1nation1day.com" target="_blank">1Nation1Day</a> has swept across three nations - mobilizing over 8,000 international volunteers, reaching more than 2.5 million people face to face, and millions more through media.</p>
            <p id="collapseOne" class="small panel-collapse collapse">Dominic authored the book Do Not Say I Am Too Young to motivate people to seize their destiny regardless of their age. Dominic’s story reverberates with inspiration and stirs hearts to dream of a life fully dedicated to impacting people. In 2010, Dominic’s wife Lindsay founded <a href="http://angelhouse.me" target="_blank">Angel House</a> – an organization that has provided 152 Angel Houses, home to over 4,500 children in India and a freedom campus that shelters and rehabilitates nearly 100 victims of human trafficking annually in Nepal. Together, they reside in Southern California with their sons Mason and Jude.</p>
